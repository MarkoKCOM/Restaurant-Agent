import { createContext, useContext, useState, type ReactNode } from "react";

export type Lang = "he" | "en";

const LangContext = createContext<{ lang: Lang; setLang: (l: Lang) => void; t: typeof translations.he }>({
  lang: "he",
  setLang: () => {},
  t: {} as typeof translations.he,
});

export function LangProvider({ children }: { children: ReactNode }) {
  const [lang, setLang] = useState<Lang>(() => {
    const saved = localStorage.getItem("sable-lang");
    return saved === "en" ? "en" : "he";
  });

  function changeLang(l: Lang) {
    setLang(l);
    localStorage.setItem("sable-lang", l);
  }

  const t = translations[lang];
  return <LangContext.Provider value={{ lang, setLang: changeLang, t }}>{children}</LangContext.Provider>;
}

export function useLang() {
  return useContext(LangContext);
}

